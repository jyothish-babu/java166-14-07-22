package studentHibernate;

public class Student {
	private int id;
	private String stname,staddress,stdepartment,gender,stphone,stemail,stpassword;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getStname() {
		return stname;
	}
	public void setStname(String stname) {
		this.stname = stname;
	}
	public String getStaddress() {
		return staddress;
	}
	public void setStaddress(String staddress) {
		this.staddress = staddress;
	}
	public String getStdepartment() {
		return stdepartment;
	}
	public void setStdepartment(String stdepartment) {
		this.stdepartment = stdepartment;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public String getStphone() {
		return stphone;
	}
	public void setStphone(String stphone) {
		this.stphone = stphone;
	}
	public String getStemail() {
		return stemail;
	}
	public void setStemail(String stemail) {
		this.stemail = stemail;
	}
	public String getStpassword() {
		return stpassword;
	}
	public void setStpassword(String stpassword) {
		this.stpassword = stpassword;
	}
	

}
